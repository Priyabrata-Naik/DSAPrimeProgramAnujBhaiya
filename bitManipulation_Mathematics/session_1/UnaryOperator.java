package bitManipulation_Mathematics.session_1;

public class UnaryOperator {
    public static void main(String[] args) {

        int a = -1;
        int b = ~a;
        int c= a<<2;

        int d = a>>1;
        int e = a>>>1;

        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
